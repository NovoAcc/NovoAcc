
package com.dupont.phoenix.commons.services.search;


public class SearchParentTag {
	
	/** The tag id. */
	private String tagId;
	
	/** The tag path. */
	private String tagPath;
	
	/**
	 * Fetches the Tag Id.
	 * @return the Tag Id.
	 */
	public String getTagId() {
		return tagId;
	}
	
	/**
	 * Sets the Tag Id.
	 * @param tagId The Tag Id to set.
	 */
	public void setTagId(String tagId) {
		this.tagId = tagId;
	}
	
	/**
	 * Fetches the Tag path.
	 * @return The Tag Path.
	 */
	public String getTagPath() {
		return tagPath;
	}
	
	/**
	 * Sets the Tag path.
	 * @param tagPath The Tag Path to set.
	 */
	public void setTagPath(String tagPath) {
		this.tagPath = tagPath;
	}
}
